package com.example.moving_organization;

public class tester {

        private String Name;
        private String Occupation;
        private int age;

    public tester() {
    }

    public tester(String name, String occupation, int age) {
        Name = name;
        Occupation = occupation;
        this.age = age;
    }

    public String getName() {
        return Name;
    }

    public String getOccupation() {
        return Occupation;
    }

    public int getAge() {
        return age;
    }





}
